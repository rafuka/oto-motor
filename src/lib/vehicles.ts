const MULTI_WORD_BRANDS = ["Alfa Romeo"];

export function getBrand(name: string): string {
  for (const b of MULTI_WORD_BRANDS) {
    if (name.startsWith(b)) return b;
  }
  return name.split(" ")[0];
}

